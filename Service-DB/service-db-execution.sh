#!/bin/bash

set -e

###############################################################################
# LOAD ENV
###############################################################################

source ./config/db.env

###############################################################################
# JQ CHECK
###############################################################################

if ! command -v jq >/dev/null 2>&1; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: jq NOT INSTALLED"
    echo "INSTALL:"
    echo "Ubuntu : sudo apt-get install -y jq"
    echo "MacOS  : brew install jq"
    echo "###############################################################################"

    exit 1

fi


###############################################################################
# LOG
###############################################################################

LOG_DIR=./logs
mkdir -p $LOG_DIR

TIMESTAMP=$(date +"%Y%m%d_%H%M%S")

LOG_FILE=$LOG_DIR/lifesync360_db_build_$TIMESTAMP.log

###############################################################################
# AWS REGION
###############################################################################

AWS_REGION=${AWS_REGION:-ap-northeast-3}


###############################################################################
# AWS LOGIN CHECK
###############################################################################

if ! aws sts get-caller-identity >/dev/null 2>&1; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: AWS LOGIN REQUIRED"
    echo "RUN:"
    echo "aws configure"
    echo "OR"
    echo "aws sso login"
    echo "###############################################################################"

    exit 1

fi



###############################################################################
# GET AURORA CLUSTER IDENTIFIER
###############################################################################

if [ -z "$AURORA_CLUSTER_ID" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET AURORA CLUSTER IDENTIFIER"
    echo "###############################################################################"

    export AURORA_CLUSTER_ID=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --query "DBClusters[?contains(DBClusterIdentifier, 'lifesync360')].DBClusterIdentifier | [0]" \
        --output text)

fi

###############################################################################
# AURORA CLUSTER VALIDATION
###############################################################################

if [ -z "$AURORA_CLUSTER_ID" ] || \
   [ "$AURORA_CLUSTER_ID" = "None" ] || \
   [ "$AURORA_CLUSTER_ID" = "null" ]; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: AURORA CLUSTER IDENTIFIER NOT FOUND"
    echo "CHECK AWS REGION OR AWS LOGIN"
    echo "###############################################################################"

    exit 1

fi

###############################################################################
# CURRENT CLUSTER
###############################################################################

echo ""
echo "###############################################################################"
echo "CURRENT AURORA CLUSTER"
echo "###############################################################################"
echo "AURORA_CLUSTER_ID : ${AURORA_CLUSTER_ID}"
echo "###############################################################################"

###############################################################################
# DB HOST AUTO DETECT
###############################################################################

if [ -z "$DB_HOST" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET AURORA DB HOST"
    echo "###############################################################################"

    export DB_HOST=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].Endpoint" \
        --output text)

fi


###############################################################################
# DB HOST VALIDATION
###############################################################################

if [ -z "$DB_HOST" ] || [ "$DB_HOST" = "None" ]; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: AURORA DB HOST NOT FOUND"
    echo "CHECK:"
    echo "1. AWS REGION"
    echo "2. AURORA_CLUSTER_ID"
    echo "3. AWS LOGIN"
    echo "###############################################################################"

    exit 1

fi

###############################################################################
# DB USER AUTO DETECT
###############################################################################

if [ -z "$DB_USER" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET AURORA DB USER"
    echo "###############################################################################"

    export DB_USER=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].MasterUsername" \
        --output text)

fi

###############################################################################
# SECRET ARN AUTO DETECT
###############################################################################

if [ -z "$SECRET_ARN" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET SECRETS MANAGER ARN"
    echo "###############################################################################"

    export SECRET_ARN=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].MasterUserSecret.SecretArn" \
        --output text)

fi

###############################################################################
# SECRET ARN VALIDATION
###############################################################################

if [ -z "$SECRET_ARN" ] || [ "$SECRET_ARN" = "None" ]; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: SECRET ARN NOT FOUND"
    echo "###############################################################################"

    exit 1

fi

###############################################################################
# DB PASSWORD AUTO DETECT
###############################################################################

if [ -z "$DB_PASSWORD" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET DB PASSWORD FROM SECRETS MANAGER"
    echo "###############################################################################"

    export DB_PASSWORD=$(aws secretsmanager get-secret-value \
        --secret-id ${SECRET_ARN} \
        --region ${AWS_REGION} \
        --query SecretString \
        --output text | jq -r '.password')

fi


###############################################################################
# DB PASSWORD VALIDATION
###############################################################################

if [ -z "$DB_PASSWORD" ] || [ "$DB_PASSWORD" = "null" ]; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: DB PASSWORD NOT FOUND"
    echo "###############################################################################"

    exit 1

fi


###############################################################################
# DB NAME AUTO DETECT
###############################################################################

if [ -z "$DB_NAME" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET DB NAME"
    echo "###############################################################################"

    export DB_NAME=$(aws secretsmanager get-secret-value \
    --secret-id ${SECRET_ARN} \
    --region ${AWS_REGION} \
    --query SecretString \
    --output text | jq -r '.dbname // empty')

fi


###############################################################################
# DB NAME DEFAULT
###############################################################################

if [ -z "$DB_NAME" ] || [ "$DB_NAME" = "null" ]; then

    echo ""
    echo "###############################################################################"
    echo "DB NAME NOT FOUND IN SECRET"
    echo "USE DEFAULT DB NAME"
    echo "###############################################################################"

    export DB_NAME="lifesync360"

fi


###############################################################################
# DB PORT DEFAULT
###############################################################################

export DB_PORT=${DB_PORT:-3306}

###############################################################################
# DB CHARSET AUTO DEFAULT
###############################################################################

export DB_CHARSET=${DB_CHARSET:-utf8mb4}

###############################################################################
# MYSQL PASSWORD ENV
###############################################################################

export MYSQL_PWD="${DB_PASSWORD}"

###############################################################################
# MYSQL BASE CMD
###############################################################################

MYSQL_CMD="mysql \
-h${DB_HOST} \
-P${DB_PORT} \
-u${DB_USER} \
--default-character-set=${DB_CHARSET}"

###############################################################################
# CONNECTION TEST
###############################################################################

echo ""
echo "###############################################################################"
echo "AURORA MYSQL CONNECTION INFO"
echo "###############################################################################"
echo "DB_HOST      : ${DB_HOST}"
echo "DB_PORT      : ${DB_PORT}"
echo "DB_NAME      : ${DB_NAME}"
echo "DB_USER      : ${DB_USER}"
echo "DB_CHARSET   : ${DB_CHARSET}"
echo "AWS_REGION   : ${AWS_REGION}"
echo "SECRET_ARN   : ${SECRET_ARN}"
echo "###############################################################################"
echo ""

###############################################################################
# MYSQL CONNECTION TEST
###############################################################################

echo ""
echo "###############################################################################"
echo "MYSQL DATABASE LIST"
echo "###############################################################################"

mysql \
-h${DB_HOST} \
-P${DB_PORT} \
-u${DB_USER} \
--default-character-set=${DB_CHARSET} \
-e "SHOW DATABASES;" \
${DB_NAME} | tee -a ${LOG_FILE}

###############################################################################
# MYSQL TABLE CHECK
###############################################################################

echo ""
echo "###############################################################################"
echo "MYSQL TABLE LIST"
echo "###############################################################################"

mysql \
-h${DB_HOST} \
-P${DB_PORT} \
-u${DB_USER} \
--default-character-set=${DB_CHARSET} \
-e "SHOW TABLES;" \
${DB_NAME} | tee -a ${LOG_FILE}


###############################################################################
# EXECUTE FUNCTION
###############################################################################

execute_sql() {

    FILE_NAME=$1

    echo ""
    echo "###############################################################################" | tee -a $LOG_FILE
    echo "EXECUTE : $FILE_NAME" | tee -a $LOG_FILE
    echo "###############################################################################" | tee -a $LOG_FILE

    START_TIME=$(date +%s)

    ###########################################################################
    # SQL EXECUTION
    ###########################################################################

    $MYSQL_CMD < $FILE_NAME 2>&1 | tee -a $LOG_FILE

    ###########################################################################
    # MYSQL EXIT CODE CHECK
    ###########################################################################

    MYSQL_EXIT_CODE=${PIPESTATUS[0]}

    if [ $MYSQL_EXIT_CODE -ne 0 ]; then

        echo ""
        echo "###############################################################################" | tee -a $LOG_FILE
        echo "ERROR : $FILE_NAME FAILED" | tee -a $LOG_FILE
        echo "MYSQL EXIT CODE : $MYSQL_EXIT_CODE" | tee -a $LOG_FILE
        echo "###############################################################################" | tee -a $LOG_FILE

        exit 1

    fi

    ###########################################################################
    # EXECUTION TIME
    ###########################################################################

    END_TIME=$(date +%s)

    ELAPSED=$((END_TIME - START_TIME))

    echo "SUCCESS : $FILE_NAME (${ELAPSED} sec)" | tee -a $LOG_FILE
}

###############################################################################
# START
###############################################################################

echo ""
echo "###############################################################################"
echo "LifeSync360 Aurora MySQL Build START"
echo "###############################################################################"

###############################################################################
# STEP 1. CREATE DATABASE & TABLES
###############################################################################

execute_sql "Aurora_MySQL_DB_Create.sql"

###############################################################################
# STEP 2. COMPANY MASTER
###############################################################################

execute_sql "1.company_master.sql"

###############################################################################
# STEP 3. CATEGORY MASTER
###############################################################################

execute_sql "2.category_master.sql"

###############################################################################
# STEP 4. BASE PRODUCT POOL
###############################################################################

execute_sql "3.base_product_pool.sql"

###############################################################################
# STEP 5. PRODUCT MASTER
###############################################################################

execute_sql "4.product_master.sql"

###############################################################################
# STEP 6. PRODUCT OPTION
###############################################################################

execute_sql "5.product_option.sql"

###############################################################################
# STEP 7. RECOMMEND RULE
###############################################################################

execute_sql "6.recommend_rule.sql"

###############################################################################
# STEP 8. CROSS SELL RULE
###############################################################################

execute_sql "7.cross_sell_rule.sql"

###############################################################################
# STEP 9. CAMPAIGN MASTER
###############################################################################

execute_sql "8.campaign_master.sql"

###############################################################################
# COMPLETE
###############################################################################

echo ""
echo "###############################################################################"
echo "LifeSync360 Aurora MySQL Build COMPLETE"
echo "LOG FILE : $LOG_FILE"
echo "###############################################################################"