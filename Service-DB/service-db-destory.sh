#!/bin/bash

set -e

###############################################################################
# LOAD ENV
###############################################################################

source ./config/db.env

###############################################################################
# LOG
###############################################################################

LOG_DIR=./logs
mkdir -p $LOG_DIR

TIMESTAMP=$(date +"%Y%m%d_%H%M%S")

LOG_FILE=$LOG_DIR/lifesync360_db_destroy_$TIMESTAMP.log

###############################################################################
# AWS REGION
###############################################################################

AWS_REGION=${AWS_REGION:-ap-northeast-3}

###############################################################################
# MYSQL CLIENT CHECK
###############################################################################

if ! command -v mysql >/dev/null 2>&1; then

    echo ""
    echo "###############################################################################"
    echo "ERROR: mysql CLIENT NOT INSTALLED"
    echo "Amazon Linux:"
    echo "sudo dnf install -y mariadb105"
    echo "###############################################################################"

    exit 1

fi

###############################################################################
# GET AURORA CLUSTER IDENTIFIER
###############################################################################

if [ -z "$AURORA_CLUSTER_ID" ]; then

    echo ""
    echo "###############################################################################"
    echo "GET AURORA CLUSTER IDENTIFIER"
    echo "###############################################################################"

    export AURORA_CLUSTER_ID=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --query "DBClusters[?contains(DBClusterIdentifier, 'lifesync360')].DBClusterIdentifier | [0]" \
        --output text)

fi

###############################################################################
# DB HOST
###############################################################################

if [ -z "$DB_HOST" ]; then

    export DB_HOST=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].Endpoint" \
        --output text)

fi

###############################################################################
# DB USER
###############################################################################

if [ -z "$DB_USER" ]; then

    export DB_USER=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].MasterUsername" \
        --output text)

fi

###############################################################################
# SECRET ARN
###############################################################################

if [ -z "$SECRET_ARN" ]; then

    export SECRET_ARN=$(aws rds describe-db-clusters \
        --region ${AWS_REGION} \
        --db-cluster-identifier ${AURORA_CLUSTER_ID} \
        --query "DBClusters[0].MasterUserSecret.SecretArn" \
        --output text)

fi

###############################################################################
# DB PASSWORD
###############################################################################

export DB_PASSWORD=$(aws secretsmanager get-secret-value \
    --secret-id ${SECRET_ARN} \
    --region ${AWS_REGION} \
    --query SecretString \
    --output text | jq -r '.password')

###############################################################################
# DB NAME
###############################################################################

export DB_NAME=${DB_NAME:-lifesync360}

###############################################################################
# MYSQL PASSWORD ENV
###############################################################################

export MYSQL_PWD="${DB_PASSWORD}"

###############################################################################
# MYSQL CMD
###############################################################################

MYSQL_CMD="mysql \
-h${DB_HOST} \
-P3306 \
-u${DB_USER} \
${DB_NAME}"

###############################################################################
# START
###############################################################################

echo ""
echo "###############################################################################"
echo "LifeSync360 Aurora MySQL Destroy START"
echo "###############################################################################"

################################################################################
# DROP DATABASE
###############################################################################

mysql \
-h${DB_HOST} \
-P3306 \
-u${DB_USER} \
-e "
SET FOREIGN_KEY_CHECKS=0;

DROP DATABASE IF EXISTS lifesync360;

SET FOREIGN_KEY_CHECKS=1;
" 2>&1 | tee -a $LOG_FILE

###############################################################################
# VERIFY
###############################################################################

echo ""
echo "###############################################################################"
echo "VERIFY TABLES"
echo "###############################################################################"

$MYSQL_CMD -e "SHOW DATABASES;" \
2>&1 | tee -a $LOG_FILE

###############################################################################
# COMPLETE
###############################################################################

echo ""
echo "###############################################################################"
echo "LifeSync360 Aurora MySQL Destroy COMPLETE"
echo "LOG FILE : $LOG_FILE"
echo "###############################################################################"